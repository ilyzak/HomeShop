package homShop;

public class Customer {
	
	

		
		String Fullname;
		String address;
		
		public Customer(String fullname,String address) {
			this.Fullname=fullname;
			this.address=address;
			
		}
		public String getFullname() {
			return Fullname;
		}
		public String getAddress() {
			return address;
		}
			

	}



